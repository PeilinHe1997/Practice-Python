#运算符的优先级
'''
优先级高的先执行、低的后执行；
相同优先级，从左到右执行；
四则运算：先乘除后加减；
运算符级别 描述 
** 幂 
+  - 正负号 
*  /    %   // 乘、除、取模、取整 
>、>=、<、<=、!=、== 比较运算符 
=、+=、-=、*=、/=、%=、**=、//= 赋值运算符 
and、or、not 逻辑运算符

(1<5) or (1==3)
3+2*(3==5)/2
(1+2)*(3==4/5)
(True == 1)*(3==4/5)

'''


print((1<5) or (1==3))

print(3+2*(3==5)/2)

print((1+2)*(3==4/5))

print((True == 1)*(3==4/5))





















