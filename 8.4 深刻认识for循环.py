# 重新认识for循环
# for i in 序列（1、字符；2、列表；3、元组；4、字典；5、集合）

print("-----------字符----------")
for i in "xsfsgge":
    print(i)

    
print("-----------元组----------")
for i in (23,22,33,44,55):
    print(i)

    
print("-----------列表----------")
for i in [23,22,33,44,55]:
    print(i)
    
    
print("-----------字典----------")
for k,v in {"青菜":53,"白菜":62}.items():
    print(k,v)

print("-----------集合----------")
for i in {23,22,33,44,55}:
    print(i)
