             # 整数类型
             
#四则混合运算优先级和数学一致，不受长度限制，只受虚拟内存限制
#整数运算可以得到浮点型
num1 = 15555
num2 = 23333
print(num1+num2)
print(num1*num2)
print(num1/num2)
print(num1-num2)

            #  浮点型
#四则混合运算优先级和数学一致，不受长度限制，只受虚拟内存限制
#浮点型和整型运算得出浮点型
num1 = 15555.23
num2 = 23333.34
print(num1+num2)
print(num1*num2)
print(num1/num2)
print(num1-num2)


